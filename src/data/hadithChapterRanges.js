// Helper function to get hadith range string
export function getHadithRange(collectionSlug, chapterNumber) {
  const chapterNum = String(chapterNumber);
  const ranges = COLLECTION_RANGES[collectionSlug];
  
  if (ranges && ranges[chapterNum]) {
    const range = ranges[chapterNum];
    return range.replace('-', ' to ');
  }
  
  return null;
}

